
// Infinite loop using while
// Uncomment to run
while (true) {
  console.log("Infinite while loop");
 }

// Infinite loop using do...while
// Uncomment to run
 do {
       console.log("Infinite do...while loop");
 } while (true);

// Infinite loop using for
// Uncomment to run
 for (;;) {
   console.log("Infinite for loop");
 }
